<h2>&#192; propos du service de navette</h2>

<p>La navette est mise &#224; la disposition des &#233;tudiants, des professeurs et du personnel pour le transport d&#8217;un campus &#224; l&#8217;autre. Ses arr&#234;ts se trouvent devant le pavillon Hall au campus du centre-ville (SGW) et devant la chapelle Loyola. Avec son trajet d&#8217;environ 20 minutes, ce service gratuit est bien plus rapide que le transport en commun.</p>
<ul>
<li>Vous devez pr&#233;senter votre carte d&#8217;identit&#233; de l&#8217;Universit&#233; pour emprunter la navette.</li>
<li>De septembre &#224; avril, les autobus fonctionnement assez r&#233;guli&#232;rement, &#224; l&#8217;exception de la p&#233;riode des examens et des vacances.</li>
<li>Les autobus sont adapt&#233;s pour les fauteuils roulants.</li>
<li>Les heures de d&#233;part annonc&#233;es sont approximatives et peuvent varier en raison de circonstances inattendues, de la circulation et des intemp&#233;ries.</li>
<li>Les autobus de remplacement ne sont pas accessibles pour les fauteuils roulants.</li>

</ul>
<p>Renseignements : 514 848-2424, poste 4636 (poste INFO)</strong>.</p>


