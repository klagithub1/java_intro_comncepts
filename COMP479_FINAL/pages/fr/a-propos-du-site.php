<h2>Contenu</h2>

<p>
Le contenu de ce site Web a &#233;t&#233; collig&#233;, r&#233;dig&#233; et r&#233;vis&#233; par le personnel de l&#8217;unit&#233; Communications Web de l&#8217;Universit&#233;, qui fait partie des Communications internes et Web, elle-m&#234;me une unit&#233; au sein du Service des communications de l&#8217;Universit&#233;. La traduction a &#233;t&#233; r&#233;alis&#233;e par le Service de traduction de l&#8217;Universit&#233; Concordia, qui rel&#232;ve du Vice-rectorat aux relations externes et secr&#233;tariat g&#233;n&#233;ral.
</p>
 

<p>
Les nouvelles sont mises &#224; jour par la r&#233;dactrice en chef de News@Concordia qui rel&#232;ve &#233;galement des Communications Web. Le calendrier des &#233;v&#233;nements provient soit de <a href="../index.html">NOW news and events</a>, soit du <a href="http://www.concordia.ca/events/">Calendrier des activit&#233;s (en anglais)</a> qui est actualis&#233; par le Service des renseignements g&#233;n&#233;raux au sein de <a href="http://web2.concordia.ca/mrkcom/">Communications-marketing</a>. Les <a href="http://now.concordia.ca/for-media/communiques-de-presse/">communiqu&#233;s de presse officiels</a> sont r&#233;dig&#233;s par les <a href="http://now.concordia.ca/for-media/">Relations m&#233;dias</a>.
</p>

<p>
Une bonne partie du contenu est tir&#233;e de publications officielles, notamment les annuaires, les panoramas et les guides &#233;tudiants de l&#8217;Universit&#233;. Les services administratifs ayant contribu&#233; au contenu sont : Gestion des effectifs &#233;tudiants, <a href="http://registrar.concordia.ca/">Registrariat</a>, <a href="../index.html">Service de la recherche</a>, <a href="http://www.concordia.ca/academics/faculties/">Facult&#233;s</a> et <a href="http://www.concordia.ca/academics/faculties/">&#201;cole des &#233;tudes sup&#233;rieures</a>, <a href="http://web2.concordia.ca/Inst_Res/IR/IR_homepage.html">Service de la planification institutionnelle</a>, <a href="http://archives3.concordia.ca/">Archives</a>, <a href="http://cdev.concordia.ca/CnD/cndstart.html">Service de consultation et d&#8217;orientation</a> et <a href="../index.html">Bureau du soutien universitaire et des relations avec les dipl&#244;m&#233;s</a>.
</p>


<h2>Conception</h2>

<p>
Les Communications Web de l&#8217;Universit&#233; ont cr&#233;&#233; le nouveau design et ont cod&#233; le HTML/CSS du site Web principal de l&#8217;Universit&#233;. Ce design a &#233;galement servi de mod&#232;le pour la cr&#233;ation des sites Web des d&#233;partements, des unit&#233;s administratives, des activit&#233;s officielles, etc. (par exemple, visitez le <a href="http://now.concordia.ca/for-media/">site Web des Relations m&#233;dias</a>). Le code pour le site et le mod&#232;le est conforme aux normes Web XHTML et CSS, et comprend un certain nombre d&#8217;options pour <a href="http://www.concordia.ca/fr/accessibilite.php">l&#8217;accessibilit&#233; Web</a>.
</p>


<p>
Au cours du processus de conception, les Communications Web ont demand&#233; l&#8217;opinion de la communaut&#233; universitaire, notamment celle de l&#8217;unit&#233; Communications-marketing, des membres du forum Web, de volontaires ainsi que des professeurs et membres du personnel qui ont assist&#233; aux pr&#233;sentations de l&#8217;unit&#233;.
</p>


<h2>Soutien technique</h2>

<p>
Le Service de d&#233;veloppement Web du Service des technologies de <a href="http://iits.concordia.ca/">l&#8217;information et de l&#8217;enseignement</a> (IITS) propose un soutien technique pour l&#8217;administration des serveurs, certaines bases de donn&#233;es et la programmation Web.
</p>


<h2>Photographie</h2>

<p>
Sauf indication contraire, les photos qui apparaissent sur le site Web proviennent de la banque d&#8217;images de Concordia, produite par les M&#233;dias cr&#233;atifs.
</p>

