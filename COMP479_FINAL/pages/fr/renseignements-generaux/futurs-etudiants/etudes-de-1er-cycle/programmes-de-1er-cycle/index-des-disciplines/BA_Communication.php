            <h1>Communication</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />


            <div class="page-title-hline">&nbsp;</div>
   <p>    Le D&eacute;partement de communication de l&rsquo;Universit&eacute;
   Concordia fournit &agrave; ses &eacute;tudiants les outils n&eacute;cessaires
   pour faire leur marque dans un paysage m&eacute;diatique de plus en plus
   concurrentiel et mondialis&eacute;. Premier du genre au Canada, le programme
   en communication de Concordia jouit d&rsquo;une r&eacute;putation
   internationale et d&rsquo;une solide exp&eacute;rience. Il pr&eacute;pare les
   &eacute;tudiants &agrave; faire carri&egrave;re dans l&rsquo;industrie des
   m&eacute;dias et de la culture en int&eacute;grant les principaux courants
   th&eacute;oriques en communication &agrave; une formation pratique en
   technologies m&eacute;diatiques. Il dispose ainsi d&rsquo;outils
   sp&eacute;cialement con&ccedil;us pour aider les &eacute;tudiants &agrave;
   exercer un regard critique sur le monde afin de mieux le saisir et d&rsquo;y
   participer de mani&egrave;re novatrice.</p>

   <p>Les &eacute;tudiants tirent profit de la convivialit&eacute; de nos
   classes peu nombreuses et de nos cours qui leur assurent une solide
   formation. Ces cours portent, entre autres, sur l&rsquo;histoire des
   technologies de la communication, la technologie des m&eacute;dias et la
   soci&eacute;t&eacute; ainsi que sur des sujets d&rsquo;actualit&eacute; tels
   la race et l&rsquo;ethnicit&eacute;, le genre, les m&eacute;dias non
   traditionnels ou la t&eacute;l&eacute;vision.  </p>

   <p>Dans le cadre des programmes de sp&eacute;cialisation et de majeure, les
   &eacute;tudiants doivent compl&eacute;ter leur formation par des cours de
   tronc commun en production sonore ou interm&eacute;diatique ainsi qu&rsquo;en
   r&eacute;alisation de films ou de vid&eacute;os. Le D&eacute;partement se
   donne en effet comme mandat principal de jeter les ponts entre th&eacute;orie
   et pratique. De par ses programmes concurrentiels et ses crit&egrave;res
   d&rsquo;admission rigoureux, seuls les meilleurs &eacute;tudiants en
   Am&eacute;rique du Nord y sont admis.</p>

      <h3><em>BA Specialization in Communication Studies</em> &mdash; 60 cr&eacute;dits</h3>
        <p>Formation ax&eacute;e sur divers m&eacute;dias offrant une
        compr&eacute;hension approfondie des processus de communication;
        destin&eacute;e aux &eacute;tudiants d&eacute;sireux de se
        sp&eacute;cialiser dans le domaine.</p>
        <h3> <em>BA Major in Communication Studies</em> &mdash; 42 cr&eacute;dits</h3>
      <p>Con&ccedil;u en vue de combiner des &eacute;tudes en communication avec
      une majeure ou une mineure dans une autre discipline.</p>

       <p>Consultez la description du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/diplomes/baccalaureat-es-arts-ba/">BA</a> pour conna&icirc;tre
      les cours obligatoires et optionnels, et l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire des
      &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la
      liste compl&egrave;te des cours.</p><br />
      <h3>Exemples de cours :</h3>
      <p><em>Communication Theory,
Communication Media: Intermedia,
Communication Analysis of Environment,
Media Policy in Canada,
Youth and Media,
Race, Ethnicity and Media,
Acoustic Communication and Design,
Media Art and Aesthetics,
Media Forecast,
Studies in Documentary,
Perspectives on the Information Society</em></p>
      <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
        M&eacute;dias et cultures; r&eacute;daction, r&eacute;alisation et
        production pour le cin&eacute;ma, la t&eacute;l&eacute;vision et la
        radio; &eacute;dition et r&eacute;daction professionnelle; politique
        m&eacute;diatique et communicationnelle; publicit&eacute; et relations
        publiques; organisations non gouvernementales dans le domaine
        humanitaire et environnemental; conception Web; d&eacute;veloppement de
        jeux interactifs; &eacute;ducation aux m&eacute;dias</p>
      <br />
