            <h1>Math&eacute;matiques pures et appliqu&eacute;es</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />


            <div class="page-title-hline">&nbsp;</div>
        <p>   Les math&eacute;matiques pures portent essentiellement sur l&rsquo;&eacute;tude des math&eacute;matiques comme fin en soi. Non pas qu&rsquo;elles n&rsquo;aient aucune application pratique, mais la motivation premi&egrave;re r&eacute;side dans leur beaut&eacute; et leur rigueur, par opposition &agrave; leur application ou &agrave; leur utilisation. Les math&eacute;matiques appliqu&eacute;es d&eacute;coulent quant &agrave; elles de la n&eacute;cessit&eacute; de r&eacute;soudre des probl&egrave;mes du monde r&eacute;el. Bien souvent, la solution &agrave; un probl&egrave;me de math&eacute;matiques appliqu&eacute;es exige une compr&eacute;hension des math&eacute;matiques pures sous-jacentes. Par cons&eacute;quent, la ligne qui les s&eacute;pare est bien mince.</p>

<p>La recherche en math&eacute;matiques pures part souvent du d&eacute;sir de comprendre le fonctionnement profond de concepts et de relations math&eacute;matiques complexes. Toutefois, elle peut &eacute;galement &ecirc;tre provoqu&eacute;e par le besoin de cr&eacute;er un nouveau mod&egrave;le afin de r&eacute;soudre un probl&egrave;me pratique et d&rsquo;en comprendre la structure interne. Les math&eacute;matiques appliqu&eacute;es servent &agrave; concevoir et &agrave; analyser des mod&egrave;les de syst&egrave;me ainsi qu&rsquo;&agrave; v&eacute;rifier et &agrave; analyser le rendement. Que ce soit en recherche et d&eacute;veloppement ou en production industrielle, les sp&eacute;cialistes des math&eacute;matiques appliqu&eacute;es travaillent en collaboration avec des ing&eacute;nieurs, des physiciens, des programmeurs et des sp&eacute;cialistes d&rsquo;autres disciplines.</p>

      <br />
      <h3><em>BA</em> or <em>BSc Honours in Pure and Applied Mathematics</em> &mdash; 66 cr&eacute;dits</h3>
      <p>Vise les &eacute;tudiants int&eacute;ress&eacute;s par la recherche et les &eacute;tudes sup&eacute;rieures. Exige la r&eacute;daction d&rsquo;une dissertation de recherche menant &agrave; l&rsquo;obtention du dipl&ocirc;me. L&rsquo;admission au programme de Honours se fait normalement apr&egrave;s avoir entrepris des &eacute;tudes en sp&eacute;cialisation, quoiqu&rsquo;il soit possible d&rsquo;y acc&eacute;der directement apr&egrave;s une entrevue. D&rsquo;excellents r&eacute;sultats sont exig&eacute;s pour entrer dans le programme et y demeurer.</p>
      <h3><em>BA</em> or <em>BSc Specialization in Pure and Applied Mathematics</em> &mdash; 60 cr&eacute;dits</h3>
      <p>Assure une solide formation en math&eacute;matiques. Les &eacute;tudiants suivent des cours de base en calcul multivariable, alg&egrave;bre lin&eacute;aire, probabilit&eacute;s et statistique, puis des cours avanc&eacute;s en analyse complexe, alg&egrave;bre abstraite, arithm&eacute;tique formelle, th&eacute;orie des nombres, probabilit&eacute;s avanc&eacute;es, statistique et analyse.</p>

     <p>Consultez la description du <a href="../diplomes/baccalaureat-es-arts-ba/index.html">BA</a> ou du <a href="../diplomes/baccalaureat-es-sciences-bsc/index.html">BSc</a> pour conna&icirc;tre les cours
     obligatoires et optionnels. </p>        <br />
      <h3>Exemples de cours :</h3>
      <p><em>Elementary Number Theory,
Combinatorics,
Abstract Algebra,
Partial Differential Equations</em></p>
      <br />
<h3>Enseignement coop&eacute;ratif</h3>
<p>Les &eacute;tudiants inscrits au programme d&rsquo;enseignement coop&eacute;ratif en math&eacute;matiques pures et appliqu&eacute;es sont g&eacute;n&eacute;ralement recrut&eacute;s pour leurs comp&eacute;tences organisationnelles et analytiques ainsi que pour leurs aptitudes &agrave; r&eacute;soudre des probl&egrave;mes. Familiers avec le concept d&rsquo;optimisation par la recherche op&eacute;rationnelle, ils doivent poss&eacute;der une solide formation en math&eacute;matiques, bien conna&icirc;tre les algorithmes de base et pouvoir faire de la programmation &agrave; l&rsquo;aide de diff&eacute;rents progiciels. Ils peuvent aussi &ecirc;tre amen&eacute;s &agrave; participer &agrave; des recherches documentaires et Internet concernant le d&eacute;veloppement de syst&egrave;mes dans le cadre d&rsquo;applications math&eacute;matiques et algorithmiques. Il leur faut &eacute;galement &ecirc;tre en mesure d&rsquo;actualiser la documentation Web accompagnant les mises &agrave; jour et les tests effectu&eacute;s.</p>


      <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
        Sciences, mod&eacute;lisation math&eacute;matique, analyse de donn&eacute;es, design, conception de logiciels, transport et communication, investissement, finance et assurance, banques, gestion des r&eacute;gimes de retraite, soci&eacute;t&eacute;s d&rsquo;&eacute;nergie</p>
