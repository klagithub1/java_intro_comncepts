            <h1>Allemand</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />



        <div class="page-title-hline">&nbsp;</div>

<p>Le langage est le v&eacute;hicule privil&eacute;gi&eacute;
d&rsquo;expression de l&rsquo;exp&eacute;rience humaine. C&rsquo;est un outil
indispensable non seulement dans les activit&eacute;s de la vie quotidienne,
mais aussi dans la manifestation de nos aspirations et r&eacute;alisations
intellectuelles. Ainsi, par l&rsquo;&eacute;tude de la langue, de la culture et
de la litt&eacute;rature, le programme d&rsquo;allemand offre de nouvelles
perspectives &agrave; nos &eacute;tudiants. Vous aurez notamment la
possibilit&eacute; de d&eacute;velopper les principales aptitudes
langagi&egrave;res : parler, &eacute;couter, lire et &eacute;crire.
Au-del&agrave; du niveau d&eacute;butant, tous les cours sont donn&eacute;s
exclusivement en allemand.</p>

<p>Apprendre une ou plusieurs langues vivantes est particuli&egrave;rement utile
sur le march&eacute; de l&rsquo;emploi actuel. La mondialisation entra&icirc;ne
un brassage linguistique comme jamais auparavant. L&rsquo;apprentissage
d&rsquo;une langue &eacute;trang&egrave;re &eacute;largit les horizons et permet
de s&rsquo;ouvrir &agrave; une immensit&eacute; de nouvelles cultures,
d&rsquo;histoires et de litt&eacute;ratures, que ce soit par
int&eacute;r&ecirc;t personnel ou dans la perspective de poursuivre des
&eacute;tudes de 2e ou 3e cycle. </p>

  <h3><em>BA Honours in German</em> &mdash; 60 cr&eacute;dits</h3>
      <h3><em>BA Major in German</em> &mdash; 42 cr&eacute;dits</h3>
      <p><em>Admission suspendue au</em> Honours <em>et &agrave; la majeure pour l&rsquo;ann&eacute;e 2010-2011</em></p>
      <h3><em>Minor in German</em> &mdash; 33 cr&eacute;dits</h3>

      <p>S&rsquo;adresse aux &eacute;tudiants d&eacute;j&agrave; inscrits
      &agrave; un programme de l&rsquo;Universit&eacute; Concordia. Tous ceux
      qui poss&egrave;dent d&eacute;j&agrave; un certain bagage langagier
      doivent contacter le D&eacute;partement afin de passer un test de
      classement appropri&eacute;. Le D&eacute;partement se r&eacute;serve le
      droit de transf&eacute;rer un &eacute;tudiant dans un cours plus
      avanc&eacute; s&rsquo;il juge que celui auquel il est inscrit ne convient
      pas &agrave; son niveau.</p>
<p>Consultez la description du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/diplomes/baccalaureat-es-arts-ba/">BA</a> pour conna&icirc;tre
      les cours obligatoires et optionnels, et l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire des
      &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la
      liste compl&egrave;te des cours.</p>  
      <br />
      <h3>Exemples de cours :</h3>
      <p><em>Introductory German, Introduction to German Culture, German Literature
      in Translation, Introduction to Modern German Literature, Topics in the
      Culture of German-Speaking Nations</em></p>
      <br />
      <h3>D&eacute;partement d&rsquo;&eacute;tudes anciennes, langues modernes et linguistique</h3>
      <p>Comme les &eacute;tudiants qui s&rsquo;inscrivent au D&eacute;partement ont des connaissances langagi&egrave;res in&eacute;gales, nos programmes sont con&ccedil;us en fonction des capacit&eacute;s et des besoins de chacun. Ainsi, il est possible par exemple d&rsquo;acqu&eacute;rir de solides bases linguistiques gr&acirc;ce aux cours de langue et de traduction.
</p>
        <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
        &Eacute;ducation, milieu des affaires, gouvernement, industrie du voyage et entreprises internationales.</p>
