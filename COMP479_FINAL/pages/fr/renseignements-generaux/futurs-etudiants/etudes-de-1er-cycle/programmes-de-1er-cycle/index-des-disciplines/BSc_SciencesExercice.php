            <h1>Sciences de l&rsquo;exercice </h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />

            <div class="page-title-hline">&nbsp;</div>
            <p>La sant&eacute; et la bonne forme physique sont essentielles &agrave; vos yeux? Vous souhaitez aider la  soci&eacute;t&eacute; &agrave; adopter un mode de vie sain? Un mode de vie inactif, voire s&eacute;dentaire, est un important facteur de risque pour la sant&eacute; et a un impact direct sur les besoins de la soci&eacute;t&eacute; en mati&egrave;re de soins. Si vous &ecirc;tes int&eacute;ress&eacute; par une carri&egrave;re o&ugrave; l&rsquo;activit&eacute; physique joue un r&ocirc;le primordial dans la promotion et le maintien d&rsquo;un mode de vie sain, dans la r&eacute;adaptation ainsi que dans les applications th&eacute;rapeutiques afin de contribuer &agrave; une meilleure sant&eacute; et forme physique, le D&eacute;partement des sciences de l&rsquo;exercice de l&rsquo;Universit&eacute; Concordia a le programme qu&rsquo;il vous faut.</p>
      <br />

      <h3><em>Major in Exercise Science</em> &mdash; 42 cr&eacute;dits</h3>
      <p>Con&ccedil;ue en vue de faire carri&egrave;re en sant&eacute;, en culture physique ou de poursuivre des &eacute;tudes sup&eacute;rieures. Assure de solides bases gr&acirc;ce &agrave; des cours appliqu&eacute;s en anatomie et physiologie humaines, physiologie de l&rsquo;exercice et biom&eacute;canique. Met l&rsquo;accent sur les sciences appliqu&eacute;es, l&rsquo;&eacute;valuation de la sant&eacute; et de la forme physique ainsi que sur la conception de programmes d&rsquo;exercices appropri&eacute;s visant la sant&eacute; et le bien-&ecirc;tre physique. </p>
      <h3><em>Honours in Exercise Science</em> &mdash; 72 cr&eacute;dits*</h3>
      <p>Bas&eacute; sur la majeure, le programme de <em>Honours</em> comporte &eacute;galement un volet recherche. Les &eacute;tudiants acqui&egrave;rent ainsi les connaissances et comp&eacute;tences essentielles pour effectuer des recherches de pointe novatrices en collaboration avec un professeur. Ils &eacute;tudient les fondements th&eacute;oriques et &eacute;thiques de la recherche et suivent un certain nombre de cours pratiques afin d&rsquo;apprendre des techniques de recherche concr&egrave;tes. Programme con&ccedil;u en vue de poursuivre des &eacute;tudes sup&eacute;rieures. .</p>
      <p>*Admission &agrave; la majeure; les &eacute;tudiants peuvent demander un transfert &agrave; la sp&eacute;cialisation apr&egrave;s avoir termin&eacute; 24 cr&eacute;dits.</p>
<p>Consultez la description du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/diplomes/baccalaureat-es-sciences-bsc/">BSc</a> pour
     conna&icirc;tre les cours obligatoires et optionnels, et l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire
     des &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la liste
     compl&egrave;te des cours.</p>             <br />
      <h3>Exemples de cours :</h3>
      <p><em>Emergency Care in Sport and Exercise,
Principles of Athletic Therapy,
Therapeutic Modalities in Sports Medicine,
Rehabilitation of the Hip, Spine and Pelvis,
Physical Growth and Maturation,
Human Anatomy I &amp; II,
Stress, Health and Disease,
Human Physiology I &amp; II,
Physical Activity, Health and Aging,
Pediatric Exercise Science,
Pathophysiology in Clinical Exercise Science I &amp; II,
Research Experience (Honours),
Honours Thesi</em>s</p>
      <br />
      <h3>D&eacute;partement des sciences de l&rsquo;exercice</h3>
      
    <p>  Ax&eacute;s sur l&rsquo;enseignement et la recherche dans les domaines
    de l&rsquo;entra&icirc;nement, de l&rsquo;activit&eacute; physique et de la
    sant&eacute;, nos programmes mettent l&rsquo;accent sur la th&eacute;rapie
    sportive et la physiologie de l&rsquo;exercice clinique. Situ&eacute; dans
    le complexe des sciences Richard-J.-Renaud au campus Loyola, le
    D&eacute;partement poss&egrave;de des installations d&rsquo;enseignement,
    d&rsquo;apprentissage et de recherche de pointe. Ses professeurs dynamiques,
    son mat&eacute;riel ultramoderne et son laboratoire d&rsquo;apprentissage
    hors pair offrent aux &eacute;tudiants une exp&eacute;rience unique.</p>

    <p>Vous pourrez ainsi explorer les processus scientifiques qui se produisent
    lorsque des personnes en sant&eacute; et d&rsquo;autres souffrant de
    diff&eacute;rentes d&eacute;ficiences, incapacit&eacute;s et maladies
    entreprennent des activit&eacute;s visant un mode de vie plus sain. Cours
    magistraux, formations en laboratoire et stages vous permettront
    d&rsquo;acqu&eacute;rir les connaissances et les comp&eacute;tences
    n&eacute;cessaires pour &eacute;valuer la sant&eacute; et la forme physique
    de tout un chacun. Vous pourrez ainsi concevoir un programme
    d&rsquo;exercices appropri&eacute; pour r&eacute;tablir, am&eacute;liorer ou
    maintenir la sant&eacute; et les capacit&eacute;s fonctionnelles tant des
    personnes souffrant d&rsquo;affections chroniques que des grands
    athl&egrave;tes. </p>

      <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
       Th&eacute;rapeute sportif, physiologiste de l&rsquo;exercice clinique, entra&icirc;neur personnel, personnel de laboratoire</p><br />
