            <h1>&Eacute;tudes fran&ccedil;aises &ndash; Langue fran&ccedil;aise (certificat)</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />

            <div class="page-title-hline">&nbsp;</div>
            <p>Le fran&ccedil;ais est aujourd&rsquo;hui la langue officielle dans pr&egrave;s de 50 pays, notamment au sein de l&rsquo;Union europ&eacute;enne. La formation pouss&eacute;e en langue fran&ccedil;aise et en fran&ccedil;ais langue seconde est un crit&egrave;re important d&rsquo;employabilit&eacute; et d&rsquo;avancement. Une formation en langue, en litt&eacute;rature, en r&eacute;daction ou en traduction ouvre les portes d&rsquo;une vaste gamme de professions : r&eacute;daction technique, am&eacute;nagement linguistique, r&eacute;vision, relations publiques, enseignement, journalisme, &eacute;dition, gestion de l&rsquo;industrie de la culture et localisation. Bon nombre de nos dipl&ocirc;m&eacute;s poursuivent des &eacute;tudes sup&eacute;rieures en traduction, en terminologie, en enseignement de la langue, en litt&eacute;rature, en droit et en administration.   </p><br />
      <h3>Certificat en langue fran&ccedil;aise  &mdash; 30 cr&eacute;dits</h3>
      <p>Les &eacute;tudiants choisissent leurs cours en consultation avec le D&eacute;partement et en fonction de leur niveau de connaissance et de leurs int&eacute;r&ecirc;ts. Certains des cr&eacute;dits obtenus dans le cadre du certificat peuvent s&rsquo;appliquer ult&eacute;rieurement &agrave; un programme de grade.</p>
<p>D&eacute;couvrez d&rsquo;autres <a href="../diplomes/certificats/index.html">certificats.</a> Consultez l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire des &eacute;tudes
      de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la liste
compl&egrave;te des cours.</p>       <br />
      <h3>Exemples de cours :</h3>
      <p><em>French Language: Elementary,</em> Initiation au fran&ccedil;ais &eacute;crit, Communication &eacute;crite, Communication orale, Le fran&ccedil;ais des affaires I et II, Histoire de la langue fran&ccedil;aise au Qu&eacute;bec, R&eacute;daction I et II, &Eacute;criture pour le Web, Cr&eacute;ation litt&eacute;raire : fiction, Introduction &agrave; la litt&eacute;rature francophone, Le XVIIe si&egrave;cle : l&rsquo;univers th&eacute;&acirc;tral, M&eacute;thodologie de la traduction, Initiation &agrave; la recherche documentaire et terminologique, Adaptation publicitaire</em></p>
      <br />
      <h3>D&eacute;partement d&rsquo;&eacute;tudes fran&ccedil;aises</em></h3>
      <p>Le D&eacute;partement d&rsquo;&eacute;tudes fran&ccedil;aises propose un vaste choix de cours de langue pour tous les niveaux, des d&eacute;butants aux francophones de souche. Il offre &eacute;galement des cours de linguistique, de fran&ccedil;ais, de fran&ccedil;ais langue seconde, de litt&eacute;rature et de traduction. Les &eacute;tudiants peuvent donc choisir des cours de langue ou de litt&eacute;rature adapt&eacute;s &agrave; leur niveau et ainsi progresser &agrave; leur propre rythme. Une formation sp&eacute;cialis&eacute;e en fran&ccedil;ais est un atout linguistique formidable qui permet d&rsquo;acc&eacute;der &agrave; toutes sortes de carri&egrave;res fascinantes.</p>
      <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
        Relations publiques, r&eacute;daction professionnelle, journalisme, traduction, industrie culturelle, &eacute;ducation, r&eacute;vision, terminologie, litt&eacute;rature, droit et administration</p>
      <br />
