      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/Certificat_LangueFrancaise_exigences.php">Exigences du Certificat en &eacute;tudes fran&ccedil;aises &ndash; Langue fran&ccedil;aise</a> : Moyennes minimales, statistiques d'admission et exigences du programme.</p>
