            <h1>Enseignement de l&rsquo;anglais, langue seconde (certificat)</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />

            <div class="page-title-hline">&nbsp;</div>
            <p>Vise les enseignants d&rsquo;exp&eacute;rience et les &eacute;tudiants qui d&eacute;sirent enseigner aux adultes au Canada ou &agrave; l&rsquo;&eacute;tranger. Con&ccedil;u pour les anglophones de naissance ou ceux qui ma&icirc;trisent l&rsquo;anglais comme des locuteurs d&rsquo;origine. Comprend des cours d&rsquo;&eacute;tude et d&rsquo;acquisition du langage, de m&eacute;thodologie ainsi qu&rsquo;un stage pratique avec des adultes. Ne satisfait pas aux exigences du brevet d&rsquo;enseignement du Qu&eacute;bec (autorisation personnelle permanente) d&eacute;cern&eacute; par le MELS. Exigence d&rsquo;admission : baccalaur&eacute;at.</p>
      <br />
      <h3><em>Certificate in the Teaching of English as a Second Language </em>&mdash;&nbsp;30&nbsp;cr&eacute;dits</h3>
      <p>Programme compos&eacute; de dix cours de tronc commun : deux cours de r&eacute;daction anglaise au D&eacute;partement d&rsquo;&eacute;tudes anglaises, des cours sur l&rsquo;enseignement de l&rsquo;anglais, langue seconde, &agrave; la base du baccalaur&eacute;at en &eacute;ducation, deux cours de m&eacute;thodologie qui explorent l&rsquo;enseignement aux adultes et un cours de stage effectu&eacute; avec des apprenants adultes.   </p>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/Certificat_TEST_exigences.php">Exigences d&rsquo;admission particuli&egrave;res </a> &ndash; En plus des exigences d&rsquo;admission g&eacute;n&eacute;rales, les programmes ci-dessus comportent des exigences suppl&eacute;mentaires qui leur sont propres. Il vous incombe d&rsquo;en discuter avec le d&eacute;partement concern&eacute;.</p>
      <p>D&eacute;couvrez d&rsquo;autres <a href="../diplomes/certificats/index.html">certificats.</a></p><br />
      <h3>Exemples de cours :</h3>
      <p><em>Phonology for Teachers,
        Modern English Grammar,
        Methodology I &amp; II,
        TESL Pedagogy: General,
        Language Acquisition,
        Teaching English for Specific Purposes,
        Testing, Evaluation and Course Design,
        Teaching Language Arts: Secondary,
        Grammar for Teachers</em></p>
      <br />
      <h3>D&eacute;partement des sciences de l'&eacute;ducation </h3>
       <p>  La demande pour l&rsquo;enseignement de l&rsquo;anglais, langue seconde ne cesse de cro&icirc;tre au Qu&eacute;bec, au Canada et partout dans le monde. Le MELS en a d&rsquo;ailleurs fait l&rsquo;une des mati&egrave;res les plus importantes du syst&egrave;me scolaire provincial. L&rsquo;apprentissage de l&rsquo;anglais, langue seconde est obligatoire &agrave; partir de la premi&egrave;re ann&eacute;e dans les &eacute;coles qu&eacute;b&eacute;coises; de plus, un grand nombre d&rsquo;adultes francophones suivent des cours d&rsquo;anglais dans des &eacute;coles de langue priv&eacute;es. </p>

<p>Le Centre de didactique de l&rsquo;anglais, langue seconde du D&eacute;partement des sciences de l&rsquo;&eacute;ducation a form&eacute; des milliers d&rsquo;enseignants depuis sa cr&eacute;ation en 1973. Les membres du corps professoral poss&egrave;dent une vaste exp&eacute;rience sur le terrain et sont pass&eacute;s ma&icirc;tres dans l&rsquo;art d&rsquo;enseigner la th&eacute;orie et la pratique de la didactique. Les &eacute;tudiants du baccalaur&eacute;at consacrent beaucoup de temps &agrave; l&rsquo;observation et &agrave; la pratique de l&rsquo;enseignement dans les &eacute;coles du Qu&eacute;bec. Les d&eacute;bouch&eacute;s sont stimulants pour les dipl&ocirc;m&eacute;s, qui deviennent professeurs, chercheurs ou administrateurs. </p>

      <br />
