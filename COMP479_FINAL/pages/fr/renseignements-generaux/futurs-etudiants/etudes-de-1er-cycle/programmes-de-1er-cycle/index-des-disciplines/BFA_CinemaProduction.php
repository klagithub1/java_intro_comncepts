            <h1>Production cin&eacute;matographique</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />


            <div class="page-title-hline">&nbsp;</div>
            <p><em><strong>Admission &agrave; l&rsquo;automne seulement
</strong></em></p>
<p>      Les programmes de production cin&eacute;matographique de la Facult&eacute; des beaux-arts de l&rsquo;Universit&eacute; Concordia sont reconnus parmi les meilleurs au Canada. Les &eacute;tudiants d&eacute;couvrent l&rsquo;esth&eacute;tique de la cr&eacute;ation ainsi que diff&eacute;rentes composantes de la production cin&eacute;matographique. En utilisant ce m&eacute;dium comme mode d&rsquo;expression culturelle, ils apprennent &agrave; trouver une &eacute;quivalence cin&eacute;matique &agrave; leurs id&eacute;es, &agrave; leurs histoires pr&eacute;f&eacute;r&eacute;es ou aux sujets qui les touchent.</p>
      <br />
      <h3><em>Specialization in Film Production</em> &mdash; 66 cr&eacute;dits</h3>
      <p>Sp&eacute;cialisation offerte &agrave; partir de la troisi&egrave;me ann&eacute;e seulement aux &eacute;tudiants inscrits &agrave; la majeure. Bonne connaissance du fran&ccedil;ais vivement recommand&eacute;e.</p>
      <h3><em>Major in Film Production</em> &mdash; 54 cr&eacute;dits</h3>
      <p>Ce programme s&rsquo;adresse &agrave; des &eacute;tudiants dot&eacute;s de maturit&eacute;, vers&eacute;s dans la culture des arts visuels et capables d&rsquo;&eacute;mettre des id&eacute;es sur le cin&eacute;ma en tant qu&rsquo;art &agrave; part enti&egrave;re. Il s&rsquo;int&eacute;resse &agrave; tous les genres et consid&egrave;re le film comme un mode d&rsquo;expression artistique. On y enseigne l&rsquo;esth&eacute;tique du septi&egrave;me art sous diff&eacute;rents angles ainsi que les rouages de la production cin&eacute;matographique. Les &eacute;tudiants sont ainsi invit&eacute;s &agrave; explorer les diverses techniques qui donneront forme &agrave; leurs id&eacute;es. De plus, le programme compte des cours tr&egrave;s vari&eacute;s sur la cin&eacute;maturgie et ses aspects techniques ainsi que des s&eacute;minaires sur le processus et la pratique artistique de la cr&eacute;ation de films..</p>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BFA_CinemaProduction_exigences.php">Exigences d&rsquo;admission particuli&egrave;res </a> &ndash; En plus des exigences d&rsquo;admission g&eacute;n&eacute;rales, les programmes ci-dessus comportent des exigences suppl&eacute;mentaires qui leur sont propres. Il vous incombe d&rsquo;en discuter avec le d&eacute;partement concern&eacute;.</p>
<p>Consultez la description du <a href="../diplomes/baccalaureat-en-beaux-arts-bfa/index.html">BFA</a> pour conna&icirc;tre les cours obligatoires et optionnels, et l&rsquo<a href="http://registrar.concordia.ca/calendar/">Annuaire des
      &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate
      Calendar)</em></a> pour la liste compl&egrave;te des
cours.</p>      <br />
      <h3>Exemples de cours :</h3>
      <p><em>Film aesthetics,
Filmmaking,
Image,
Sound,
Montage,
Acting and directing for the screen,
Writing for film,
Production design</em></p>
      <br />
     <h3>Cinema &mdash; &Eacute;cole de cin&eacute;ma Mel-Hoppenheim</h3>
      <p>Les programmes de cin&eacute;ma de Concordia se distinguent du fait
      qu&rsquo;ils sont offerts par la Facult&eacute; des beaux-arts et que la
      mati&egrave;re est d&rsquo;abord trait&eacute;e comme moyen
      d&rsquo;expression artistique. Aussi ont-ils pour objet de former des
      cin&eacute;astes, des cin&eacute;astes d&rsquo;animation, des historiens
      et des critiques de cin&eacute;ma capables de reconna&icirc;tre le
      potentiel artistique et culturel du m&eacute;dium, son histoire et ses
      traditions. Ax&eacute;s sur l&rsquo;art et la culture plut&ocirc;t que sur
      l&rsquo;industrie et le commerce, nos programmes enseignent
      n&eacute;anmoins les aspects techniques, financiers et commerciaux du
      cin&eacute;ma. Ils recherchent l&rsquo;&eacute;quilibre entre le
      caract&egrave;re pratique et les consid&eacute;rations purement
      artistiques du cin&eacute;ma, tout comme ils tentent de concilier les
      pr&eacute;occupations sociales et esth&eacute;tiques.</p>
   
      <br />
      <h3>Installations et co&ucirc;ts</h3>
           <p>Les appareils suivants sont mis &agrave; la disposition des &eacute;tudiants selon leur degr&eacute; d&rsquo;avancement dans les cours : cam&eacute;ras Bolex 16 mm, cam&eacute;ras Arrifex SR 16 mm, cam&eacute;ras 35 mm synchronis&eacute;es et non synchronis&eacute;es, mat&eacute;riel de montage Steenbeck et appareils d&rsquo;enregistrement num&eacute;rique, mat&eacute;riel d&rsquo;&eacute;clairage, tr&eacute;pieds &agrave; t&ecirc;te fluide, bancs d&rsquo;animation Oxberry, tireuses optiques et laboratoire d&rsquo;animation pour projets num&eacute;riques. Du mat&eacute;riel ultramoderne pour l&rsquo;enregistrement et le mixage num&eacute;riques dans un environnement Protools est fourni pour les cours de sonorisation et les projets de troisi&egrave;me ann&eacute;e. </p>

<p>Les &eacute;tudiants inscrits aux programmes d&rsquo;animation
cin&eacute;matographique et de production cin&eacute;matographique doivent
assumer les frais de la pellicule, du traitement, du tirage et du
mat&eacute;riel connexe. Selon le projet, les &eacute;tudiants en production
cin&eacute;matographique devraient pr&eacute;voir d&eacute;penser entre 1 000 $
et 2 000 $ la premi&egrave;re ann&eacute;e de cours et de 1&nbsp;500 $ &agrave; 5 000
$ la deuxi&egrave;me ou la troisi&egrave;me ann&eacute;e de cours. Pour un
nombre restreint de projets de deuxi&egrave;me et troisi&egrave;me
ann&eacute;es, l&rsquo;&Eacute;cole de cin&eacute;ma offre des subventions
modiques afin d&rsquo;att&eacute;nuer certains co&ucirc;ts de production. Les
&eacute;tudiants en animation cin&eacute;matographique doivent payer des frais
de laboratoire. D&rsquo;autres frais peuvent s&rsquo;appliquer.</p>

