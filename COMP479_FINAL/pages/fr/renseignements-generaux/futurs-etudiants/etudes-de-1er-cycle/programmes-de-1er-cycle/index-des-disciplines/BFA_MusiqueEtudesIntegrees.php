            <div class="page-title-hline">&nbsp;</div>
            <p><em><strong>Admission &agrave; l&rsquo;automne seulement
</strong></em></p>
     <p> Particuli&egrave;rement adapt&eacute; aux personnes d&eacute;sireuses
     d&rsquo;&eacute;tudier un vaste &eacute;ventail de disciplines musicales,
     ce programme offre &agrave; la fois souplesse et &eacute;tudes
     approfondies. Exemples de cours de premi&egrave;re ann&eacute;e :
     <em>Theory/Aural Training, Private Study, Music History</em> ainsi que des cours
     optionnels en musique et d&rsquo;ordre g&eacute;n&eacute;ral.</p>

     <p>Le reste des cours de la majeure consiste en des cr&eacute;dits en <em>Music
     Composition, Aural Perception, Music History, Choir,</em> en plus d&rsquo;autres
     cours optionnels en musique et hors facult&eacute;. Parmi les cours
     optionnels en musique figurent des cours d&rsquo;interpr&eacute;tation en
     jazz et musique classique, de th&eacute;orie, d&rsquo;arrangement et
     composition, d&rsquo;&eacute;lectroacoustique, de technologie musicale et
     d&rsquo;histoire de la musique. </p>

     <p>Les &eacute;tudiants sont d&rsquo;abord admis &agrave; la <em>Major in
     Integrative Music Studies.</em> Apr&egrave;s avoir r&eacute;ussi les cours de
     tronc commun de la premi&egrave;re ann&eacute;e, ils peuvent demander de
     passer &agrave; une des sp&eacute;cialisations propos&eacute;es par le
     d&eacute;partement :</p>
<ul>
	<li><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BFA_Jazz.php">&Eacute;tudes en jazz</a></li>
	<li><a href="BFA_MusiqueComposition.php">Composition musicale</a></li>
	<li><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BFA_MusiquePerformance.php">Interpr&eacute;tation musicale</a></li>
</ul>

      <br />
      <h3><em>Major in Integrative Music Studies</em> &mdash; 54 cr&eacute;dits</h3>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BFA_MusiqueEtudesIntegrees_exigences.php">Exigences d&rsquo;admission particuli&egrave;res </a> &ndash; En plus des exigences d&rsquo;admission g&eacute;n&eacute;rales, les programmes ci-dessus comportent des exigences suppl&eacute;mentaires qui leur sont propres. Il vous incombe d&rsquo;en discuter avec le d&eacute;partement concern&eacute;.</p>
<p>Consultez la description du <a href="../diplomes/baccalaureat-en-beaux-arts-bfa/index.html">BFA</a> pour conna&icirc;tre les cours obligatoires et optionnels, et l&rsquo<a href="http://registrar.concordia.ca/calendar/">Annuaire des
      &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate
      Calendar)</em></a> pour la liste compl&egrave;te des
cours.</p>         <br />
      <h3>Exemples de cours :</h3>
      <p><em>Melody and Counterpoint,
Music Composition,
Jazz Arranging,
Jazz Ensemble,
Jazz Improvisation,
Private Study</em></p>
      <br />
      <h3>Formations musicales</h3>
  <p> <strong>Ensembles de jazz</strong></p>
   <p>Chaque ann&eacute;e, des ensembles de jazz &ndash; grands orchestres, petits ensembles, ensembles de guitares, chorales jazz, petits ensembles instrumentaux et vocaux et classes de r&eacute;pertoire vocal jazz &ndash; donnent des r&eacute;citals de jazz et ex&eacute;cutent des &oelig;uvres traditionnelles et de nouvelles compositions. Des concerts par les &eacute;tudiants en improvisation jazz et des projets sp&eacute;ciaux sont &eacute;galement pr&eacute;sent&eacute;s dans la salle de concert Oscar-Peterson.</p>

<p><strong>Ensembles de musique de chambre</strong></p>
<p>Chaque ann&eacute;e, les &eacute;tudiants se produisent, en rapport avec les cours offerts par le D&eacute;partement, dans des concerts de musique de chambre vocale et instrumentale de toutes les &eacute;poques, en duo, en trio et dans le cadre de grands ensembles.</p>

<p><strong>Chant</strong></p>
<p>Des &eacute;tudiants en chant donnent r&eacute;guli&egrave;rement des r&eacute;citals d&rsquo;&oelig;uvres puis&eacute;es dans le r&eacute;pertoire traditionnel de la musique occidentale, de l&rsquo;op&eacute;ra et du jazz.</p>

<p><strong>Chorale de l&rsquo;Universit&eacute;</strong></p>
<p>L&rsquo;Universit&eacute; est dot&eacute;e de deux chorales. La grande chorale et le ch&oelig;ur de chambre donnent chaque ann&eacute;e plusieurs r&eacute;citals de chants du XVIe au XXIe si&egrave;cle. Ces chorales se produisent parfois avec l&rsquo;orchestre de l&rsquo;Universit&eacute;. Tous les membres de la communaut&eacute; universitaire de Concordia peuvent y participer (&eacute;tudiants, professeurs et membres du  personnel), ainsi que le grand public. On peut demander aux chanteurs de passer une audition pour &eacute;valuer leurs aptitudes.</p>

<p><strong>Lectures et concerts des &eacute;tudiants en jazz et composition</strong></p>
<p>Le D&eacute;partement propose des s&eacute;ances informelles, des lectures et des concerts annuels d&rsquo;&oelig;uvres des &eacute;tudiants en jazz et en composition de Concordia.</p>

<p><strong>Concerts &eacute;lectroacoustiques</strong></p>
<p>Depuis plus de 25 ans, et avec plus de 300 concerts &agrave; son actif, l&rsquo;Universit&eacute; Concordia jouit d&rsquo;une excellente r&eacute;putation &agrave; l&rsquo;&eacute;chelle nationale pour la qualit&eacute; et la diversit&eacute; de ses prestations &eacute;lectroacoustiques, qui comprennent des &oelig;uvres de compositeurs canadiens et &eacute;trangers et de jeunes artistes &eacute;mergents qui utilisent un syst&egrave;me de projection sonore &agrave; 18 bandes.</p>

      <br />
<h3>Installations</h3>
<p>Les cours de musique sont donn&eacute;s dans les installations &agrave; la fine pointe de la technologie du nouveau pavillon MB au campus Sir-George-Williams. Con&ccedil;ues pour r&eacute;pondre aux besoins du D&eacute;partement par un acousticien, elles comprennent des salles de classe &laquo; intelligentes &raquo; et des studios &eacute;quip&eacute;s pour le multim&eacute;dia, le mixage et l&rsquo;&eacute;coute en r&eacute;seau, une salle d&rsquo;enregistrement et de reproduction, un studio octophonique, un laboratoire de claviers ainsi que des modules de r&eacute;p&eacute;tition insonoris&eacute;s.</p>
<p>	Chaque ann&eacute;e, plus de cent concerts et repr&eacute;sentations, sans compter les nombreux s&eacute;minaires et cours de ma&icirc;tre, sont donn&eacute;s au campus Loyola dans la salle de concert Oscar Peterson. V&eacute;ritable chef-d&rsquo;&oelig;uvre acoustique et visuel, cette salle compte 600 places et un studio d&rsquo;enregistrement multipiste num&eacute;rique. C&rsquo;est d&rsquo;ailleurs un lieu de premier choix pour les spectacles d&rsquo;&eacute;tudiants et de professeurs, les concerts de jazz, de musique classique ou &eacute;lectroacoustique de renomm&eacute;e internationale, ainsi que pour les radiodiffusions et les enregistrements de disques compacts.</p>
      
      <br />
<h3>D&eacute;partement de musique</h3>
<p>Le D&eacute;partement offre aux &eacute;tudiants de formations diverses un environnement propice &agrave; l&rsquo;interpr&eacute;tation, &agrave; la composition et &agrave; l&rsquo;improvisation dans des genres vari&eacute;s comme le jazz, le classique et l&rsquo;&eacute;lectroacoustique. L&rsquo;accent mis sur la musique contemporaine s&rsquo;appuie sur divers styles et genres ant&eacute;rieurs examin&eacute;s au moyen de l&rsquo;analyse, de l&rsquo;historique et de l&rsquo;interpr&eacute;tation. Cours magistraux, ateliers, pratique musicale, le&ccedil;ons individuelles, tutorats, ensembles musicaux et r&eacute;p&eacute;titions constituent la structure des cours.</p>


