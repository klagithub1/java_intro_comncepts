            <h1>&Eacute;tudes int&eacute;gr&eacute;es en musique</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

