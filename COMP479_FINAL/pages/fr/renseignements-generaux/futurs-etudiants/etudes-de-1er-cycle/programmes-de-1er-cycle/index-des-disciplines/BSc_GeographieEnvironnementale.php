            <h1>G&eacute;ographie environnementale </h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />

            <div class="page-title-hline">&nbsp;</div>
           <p>Destin&eacute;s aux &eacute;tudiants ayant suivi une formation scientifique au c&eacute;gep ou au secondaire, les programmes suivants se concentrent sur l&rsquo;environnement biophysique tout en inculquant de solides connaissances en interventions humaines, politique et gestion.</p>

<p>La g&eacute;ographie environnementale est la branche de la g&eacute;ographie qui cerne les aspects spatiaux des interactions entre les humains et le monde naturel. Elle exige une compr&eacute;hension des dynamiques de diff&eacute;rents domaines, comme la climatologie, l&rsquo;hydrologie, la biog&eacute;ographie, la g&eacute;ologie et la g&eacute;omorphologie, ainsi que de la fa&ccedil;on dont les soci&eacute;t&eacute;s humaines conceptualisent l&rsquo;environnement. La g&eacute;ographie environnementale regroupe un ensemble important d&rsquo;outils analytiques destin&eacute;s &agrave; &eacute;valuer l&rsquo;impact de la pr&eacute;sence des humains sur l&rsquo;environnement en mesurant les r&eacute;sultats de l&rsquo;activit&eacute; humaine sur la topographie et les cycles naturels. Le programme s&rsquo;appuie sur des cours en g&eacute;ographie physique, notamment un groupe de cours distinct qui aborde les questions environnementales, les ressources et la gestion. Les m&eacute;thodes d&rsquo;enseignement et d&rsquo;apprentissage sont diverses, allant de cours magistraux et tutorats jusqu&rsquo;aux travaux pratiques en cartographie, en syst&egrave;me d&rsquo;information g&eacute;ographique, en t&eacute;l&eacute;d&eacute;tection et en statistique. 
      </p><br />
      <h3><em>Major in Environmental Geography</em> &mdash; 45 cr&eacute;dits</h3>
      <p>Con&ccedil;ue pour les &eacute;tudiants ayant un fort int&eacute;r&ecirc;t pour l&rsquo;environnement et la g&eacute;ographie et qui ne souhaitent pas faire carri&egrave;re dans la recherche ou poursuivre des &eacute;tudes sup&eacute;rieures.</p>
      <h3><em>Minor in Environmental Geography</em> &mdash; 24 cr&eacute;dits</h3>
      <p>Vise principalement les &eacute;tudiants d&eacute;sireux de comprendre l&rsquo;environnement et inscrits &agrave; un programme connexe en biologie, &eacute;cologie et g&eacute;nie environnemental.</p>
 <p>Consultez la description du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/diplomes/baccalaureat-es-sciences-bsc/">BSc</a> pour
     conna&icirc;tre les cours obligatoires et optionnels, et l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire
     des &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la liste
     compl&egrave;te des cours.</p>        <br />
      <h3>Exemples de cours :</h3>
      <p><em>World Geography,
        Global Environmental Issues,
        Geography of Global Change,
        The Human Environment: Place, Space, and Identity,
        Environmental Management,
        Environmental Impact Assessment,
        Geography and Public Policy,
        Geographic Information Systems,
        Introduction to Environmental Geology,
        The Natural Environment,
        Hydrology,
        The Climate System,
        Environmental Modelling, Climate Change,
        Representation Methods in Urban Studies,
        Analytical Methods in Urban Studies,
        Law and Regulation in Urban Planning,
        Urban Systems,
        Urban Ecology,
        Industrial Restructuring,
        Urban Planning in the Developing World</em></p>
      <br />
      <h3>D&eacute;partement de g&eacute;ographie, urbanisme et environnement</h3>
       
<p>Vous &ecirc;tes curieux de l&rsquo;influence que nous pouvons exercer sur l&rsquo;environnement? L&rsquo;avenir de la plan&egrave;te, les changements climatiques, les effets de la croissance d&eacute;mographique, industrielle et urbaine sur la durabilit&eacute; vous pr&eacute;occupent? Vous aimez &agrave; la fois les sciences naturelles et sociales? Collecter et analyser des donn&eacute;es, &eacute;tudier des cartes, trouver des solutions et apprendre &agrave; utiliser les plus r&eacute;centes applications d&rsquo;analyse graphique vous passionnent? Si vous souhaitez apporter votre contribution &agrave; la qualit&eacute; de nos milieux naturels et b&acirc;tis, la g&eacute;ographie, l&rsquo;urbanisme et l&rsquo;environnement sont faits pour vous. En effet, notre cursus combine harmonieusement formation th&eacute;orique, technique et appliqu&eacute;e. Il comporte trois volets &ndash; g&eacute;ographie, urbanisme et environnement &ndash; et sensibilise aux enjeux environnementaux et g&eacute;ographiques. Nos programmes visent ainsi &agrave; former des dipl&ocirc;m&eacute;s &eacute;clair&eacute;s, &agrave; m&ecirc;me de bien s&rsquo;exprimer, afin qu&rsquo;ils se consacrent &agrave; am&eacute;liorer la qualit&eacute; et la durabilit&eacute; des milieux naturels et b&acirc;tis.</p>

        
      <br />
      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
        Chercheur &agrave; Environnement Canada ou &agrave; la Commission onusienne sur la biodiversit&eacute;; am&eacute;nagiste des for&ecirc;ts; gestionnaire de parcs; planificateur de d&eacute;chets dangereux; analyste des syst&egrave;mes d&rsquo;information g&eacute;ographique et de t&eacute;l&eacute;d&eacute;tection; sp&eacute;cialiste en sciences de l&rsquo;environnement, sciences de la Terre, planification hydrologique et &eacute;cologique, immobilier, services sociaux, gestion foresti&egrave;re et fluviale, changements climatiques et environnementaux, &eacute;missions atmosph&eacute;riques ou fragmentation du paysage; garde forestier; employ&eacute; du volet g&eacute;ographique de Statistique Canada, des minist&egrave;res provinciaux de l&rsquo;agriculture ou de la foresterie; am&eacute;nageur; expert en commerce international; planificateur de transports ou  sp&eacute;cialiste des milieux et des march&eacute;s</p>
      <br />
