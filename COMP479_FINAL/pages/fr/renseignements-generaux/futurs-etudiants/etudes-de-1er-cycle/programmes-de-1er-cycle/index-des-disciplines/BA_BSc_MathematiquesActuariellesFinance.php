            <h1>Math&eacute;matiques actuarielles <br />et finance</h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />


            <div class="page-title-hline">&nbsp;</div>
            <p>Le D&eacute;partement de math&eacute;matiques et de statistique de la Facult&eacute; des arts et des sciences et le D&eacute;partement de finance de l'&eacute;cole de gestion John-Molson ont conjugu&eacute; leurs efforts pour offrir un programme de <em>BA/BSc Specialization in Actuarial Math&eacute;matiques/Finance.</em> Celui-ci s'adresse aux &eacute;tudiants ayant d'excellents r&eacute;sultats et dot&eacute;s des connaissances n&eacute;cessaires pour &ecirc;tre accept&eacute;s au programme de sp&eacute;cialisation. De plus, il ouvre un cr&eacute;neau tr&egrave;s int&eacute;ressant aux dipl&ocirc;m&eacute;s puisqu'il y a actuellement p&eacute;nurie d'experts dans ces deux domaines.</p>
      <br />
      <h3><em>BA or BSc Specialization in Actuarial Math&eacute;matiques/Finance</em> &mdash; 
      84 cr&eacute;dits</h3>
      <p>Solide formation en math&eacute;matiques actuarielles combin&eacute;e &agrave; l'&eacute;quivalent d'une mineure en finance, destin&eacute;e &agrave; ceux qui souhaitent devenir actuaires tout en se sp&eacute;cialisant dans les aspects financiers de la pratique. Les exigences du programme comprennent les cours obligatoires du <em>BA/BSc in Actuarial Math&eacute;matiques</em> (60 cr&eacute;dits) et 30 cr&eacute;dits obligatoires en comptabilit&eacute;, sciences &eacute;conomiques et finance. Seuls quinze &eacute;tudiants poss&eacute;dant un excellent dossier scolaire sont admis au programme. </p>
      <p>V&eacute;rifiez les exigences d'admission suppl&eacute;mentaires du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BA_BSc_MathematiquesActuariellesFinance_BA_exigences.php">BA</a> et du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BA_BSc_MathematiquesActuariellesFinance_BSc_exigences.php">BSc</a> &ndash; en plus des exigences d'admission g&eacute;n&eacute;rales, les programmes ci-dessus comportent des exigences qui leur sont propres.</p>
      <p>Consultez la description du <a href="../diplomes/baccalaureat-es-arts-ba/index.html">BA</a> ou du <a href="../diplomes/baccalaureat-es-sciences-bsc/index.html">BSc</a> pour conna&icirc;tre les cours obligatoires et optionnels, et l'<a href="http://registrar.concordia.ca/calendar/">Annuaire des &eacute;tudes de 1<sup>er</sup> cycle <em>(Undergraduate Calendar)</em></a> pour la liste compl&egrave;te des cours.</p>
       <br />
       <h3>Exemples de cours :</h3>
       <p><em>Math&eacute;matiques of Finance,
         Actuarial Math&eacute;matiques I &amp; II,
         Risk Theory,
         Credibility Theory,
         Loss Distributions</em></p>
       <br />
       <h3>Enseignement coop&eacute;ratif</h3>
       <p>Le programme d'enseignement coop&eacute;ratif en math&eacute;matiques actuarielles permet de se familiariser avec les programmes informatiques utilis&eacute;s par les actuaires pour analyser des donn&eacute;es socio-&eacute;conomiques, calculer des r&eacute;gimes de prestations d&eacute;termin&eacute;es et cr&eacute;er des tables actuarielles. Les &eacute;tudiants coop apprennent ainsi &agrave; r&eacute;diger des rapports professionnels et s'initient &agrave; la pr&eacute;paration de documents. Les t&acirc;ches que leur confient les employeurs &ndash; compagnies d'assurances et cabinets-conseils, par exemple &ndash; se rapprochent souvent de celles des aides-actuaires ou des analystes en actuariat. Les stagiaires travaillent donc dans un milieu professionnel structur&eacute; qui favorise le travail d'&eacute;quipe et la responsabilisation, et am&eacute;liorent de ce fait leur sens de la communication.</p>
     <br />
       <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Possibilit&eacute;s de carri&egrave;re</strong><br />
         Sciences, mod&eacute;lisation math&eacute;matique, analyse de donn&eacute;es, design, conception de logiciels, transport et communication, investissement, finance et assurance, banques, gestion des r&eacute;gimes de retraite, soci&eacute;t&eacute;s d'&eacute;nergie</p>
     <br />
       <h3>Exigences d'admission</h3>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BA_BSc_MathematiquesActuariellesFinance_BA_exigences.php">Exigences du <em>Honours</em> et de la <em>Specialization in Actuarial Math&eacute;matiques/Finance (BA) </em></a>: Moyennes minimales, statistiques d'admission et exigences du programme.</p>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/BA_BSc_MathematiquesActuariellesFinance_BSc_exigences.php">Exigences du <em>Honours</em> et de la <em>Specialization in Actuarial Math&eacute;matiques/Finance (BSc) </em></a>: Moyennes minimales, statistiques d'admission et exigences du programme.</p>
      <p><a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/exigences-dadmission/">Directives relatives &agrave; l'admission au 1<sup>er</sup> cycle</a> : Renseignements sur l'admission des &eacute;tudiants internationaux, les documents requis, les comp&eacute;tences linguistiques, le calendrier des admissions, les crit&egrave;res de s&eacute;lection et la cote de rendement.</p> 
