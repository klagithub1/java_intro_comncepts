            <h1>G√©nie √©lectrique  </h1>
<div class="sigbox" style="font-size:120%">
<a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" title="consultez le Guide des Ètudes de 1er cycle 2012-2013" style="float:left; margin-right:14px; background-color:#FFF"><img src="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/imgs/admissions-guide-thumb.jpg" width="62" height="80" alt="" class="framed" /></a>
<em style="display:block; margin-top:5px; line-height:1.4em;">Veuillez noter que les descriptions de cours n'ont pas encore &eacute;t&eacute; mis &agrave; jour. Plus voir les descriptions les plus r&eacute;centes, consultez le <a href="http://registrar.concordia.ca/pdf/GuideEtudes1213.pdf" rel="pdf">Guide des &eacute;tudes de 1<sup>er</sup> cycle 2012-2013</a></em>

	<div class="clear">&nbsp;</div>
</div> 
<br />


            <div class="page-title-hline">&nbsp;</div>
            <p>Le g&eacute;nie &eacute;lectrique s&rsquo;int&eacute;resse principalement &agrave; l&rsquo;&eacute;nergie et &agrave; l&rsquo;information ainsi qu&rsquo;aux m&eacute;thodes les plus efficaces et les plus fiables pour les convertir et les transmettre. Il englobe les &eacute;l&eacute;ments suivants : &eacute;lectronique, conception de circuits int&eacute;gr&eacute;s standard et &agrave; tr&egrave;s grande &eacute;chelle, implantation et essai, syst&egrave;mes de commande, robotique, simulation de syst&egrave;mes, t&eacute;l&eacute;communications et traitement des signaux, conception de mat&eacute;riels et de logiciels syst&egrave;mes, dispositifs et syst&egrave;mes d&rsquo;alimentation, syst&egrave;mes &eacute;lectrom&eacute;caniques, dispositifs micro&eacute;lectrom&eacute;caniques, antennes, dispositifs et circuits &agrave; micro-ondes, compatibilit&eacute; &eacute;lectromagn&eacute;tique, lasers et opto&eacute;lectronique.</p>
      <br />
      <h3><em>BEng in Electrical Engineering</em> &mdash; 120 cr&eacute;dits</h3>
      <ul>
    	<li><strong>Option Telecommunications</strong> : permet d&rsquo;acqu&eacute;rir une expertise en logiciels de t&eacute;l&eacute;communications, r&eacute;seau, traitement des signaux et technologie sans fil.</li>
		<li><strong>Option Electronics/Systems*</strong> : m&egrave;ne &agrave; une carri&egrave;re dans un domaine sp&eacute;cialis&eacute; du g&eacute;nie &eacute;lectrique : g&eacute;nie de l&rsquo;&eacute;nergie, &eacute;lectronique et int&eacute;gration &agrave; tr&egrave;s grande &eacute;chelle, syst&egrave;mes de commande, g&eacute;nie des micro-ondes, communications ou syst&egrave;mes informatiques.
<br />
          <br />          
          <strong>*</strong>Pour r&eacute;pondre &agrave; la demande d&rsquo;ing&eacute;nieurs sp&eacute;cialis&eacute;s en &eacute;nergie, six universit&eacute;s qu&eacute;b&eacute;coises, dont Concordia, se sont associ&eacute;es &agrave; Hydro-Qu&eacute;bec afin de cr&eacute;er l&rsquo;Institut en g&eacute;nie de l&rsquo;&eacute;nergie &eacute;lectrique. Les &eacute;tudiants admis &agrave; l&rsquo;Institut doivent suivre six cours offerts par les &eacute;tablissements participants et s&rsquo;y inscrire par l&rsquo;interm&eacute;diaire de leur universit&eacute; d&rsquo;origine. Certains cours se donnent en anglais et en fran&ccedil;ais. </li>
      </ul>
<p>Consultez la description du <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/diplomes/baccalaureat-en-genie-beng/">BEng</a> pour conna&icirc;tre les cours
     obligatoires et optionnels, et l&rsquo;<a href="http://registrar.concordia.ca/calendar/">Annuaire des &eacute;tudes de 1er
     cycle <em>(Undergraduate Calendar)</em></a> pour la liste compl&egrave;te des cours.</p>      <br />
      <h3>Exemples de projets :</h3>
        <ul>
	<li>Robot ROBOWARS</li>
	<li>Syst&egrave;me de conversion d&rsquo;&eacute;nergie &eacute;olienne</li>
	<li>Conception d&rsquo;une antenne sans fil destin&eacute;e aux applications int&eacute;rieures</li>
	<li>Simulation de la colonne vert&eacute;brale</li>
	<li>Immeuble intelligent utilisant la transmission sans fil</li>
      </ul>
      <br />

     <h3> Enseignement coop&eacute;ratif</h3>
<p>L&rsquo;enseignement coop&eacute;ratif en g&eacute;nie &eacute;lectrique permet d&rsquo;analyser la consommation d&rsquo;&eacute;nergie et la mesure de puissance. Il offre &eacute;galement la possibilit&eacute; d&rsquo;aider l&rsquo;&eacute;quipe technique &agrave; concevoir des cartes &eacute;volu&eacute;es dans des domaines multidisciplinaires : matrice pr&eacute;diffus&eacute;e programmable, terminal optique de pr&eacute;amplification, Datacom, r&eacute;gulation, int&eacute;grit&eacute; des signaux et compatibilit&eacute; &eacute;lectromagn&eacute;tique. Au fur et &agrave; mesure qu&rsquo;ils progressent, les &eacute;tudiants coop peuvent participer au processus de conception et de r&eacute;solution de probl&egrave;mes en g&eacute;nie, pr&eacute;parer des avant-projets et des rapports, y compris les cahiers des charges et les plans, ou encore valider et optimiser des prototypes sur le terrain.</p>

<h3>Exemples de cours :</h3>
<ul>
	<li><em>Introduction to Discrete Mathematics</em></li>
	<li><em>Programming Methodology</em></li>
	<li><em>Computer Organization and Software</em></li>
	<li><em>Fundamentals of Applied Electromagnetics</em></li>
	<li><em>Introduction to Semiconductor Materials and Devices</em></li>
	<li><em>Fundamentals of Electrical Power Engineering</em></li>
</ul>

      <p style="border:1px solid #CCC; background-color:#EFEFEF; padding:10px;"><strong>Nos dipl&ocirc;m&eacute;s font carri&egrave;re </strong> electronics, telecommunications,
en &eacute;lectronique, t&eacute;l&eacute;communications, syst&egrave;mes d&rsquo;alimentation, robotique, conception de circuits int&eacute;gr&eacute;s, g&eacute;nie des micro-ondes ou syst&egrave;mes informatiques. Ils travaillent notamment pour Alcatel, CAE inc., Honeywell et IBM.</p>
