<p>
Au moment de la conception du nouveau site Web de l&#8217;Universit&#233;, ainsi que des mod&#232;les pour les sites des d&#233;partements, services et unit&#233;s, nous avons pris en compte les normes d&#8217;accessibilit&#233; afin que le plus large public possible puisse l&#8217;utiliser. Nous souhaitons ainsi que le site soit accessible non seulement aux personnes qui font usage des technologies d&#8217;assistance, telles que les lecteurs d&#8217;&#233;cran, mais aussi &#224; celles qui poss&#232;dent un navigateur ou un ordinateur moins r&#233;cent et une vitesse de connexion Internet lente.
</p>

<p>
Conform&#233;ment aux <a href="http://www.w3.org/TR/WCAG/">Directives pour l&#8217;accessibilit&#233; aux contenus Web</a>, nous avons b&#226;ti nos pages de mani&#232;re &#224; ce qu&#8217;elles se &#171; transforment gracieusement &#187;. En d&#8217;autres termes, nous avons : 
</p>

<ul>
	<li>s&#233;par&#233; la structure de la pr&#233;sentation. Nous utilisons des feuilles de style en cascade afin de g&#233;rer toutes les pr&#233;sentations. La d&#233;sactivation/fermeture de la feuille de style affectera la pr&#233;sentation visuelle, mais les diverses fonctions du site et la navigation restent en &#233;tat de marche;</li>

	<li>cr&#233;&#233; des documents qui fonctionnent m&#234;me si l&#8217;utilisateur ne peut pas voir ou entendre. Tout le contenu est fourni en texte; lorsqu&#8217;il y a des images ou des documents audio ou vid&#233;o, l&#8217;utilisateur aura des options &#233;quivalentes. Les personnes aveugles peuvent se servir d&#8217;un lecteur d&#8217;&#233;cran pour obtenir l&#8217;information textuelle contenue dans une page. </li>

	<li>con&#231;u des documents qui ne comptent pas que sur un type de mat&#233;riel. Les utilisateurs peuvent acc&#233;der au site &#224; partir de diff&#233;rents navigateurs, r&#233;solutions d&#8217;&#233;cran et options, avec ou sans souris, etc. Consulter la section ci-dessous pour des renseignements sur les raccourcis et les raccourcis clavier.</li>

</ul>

<p>Nous nous sommes &#233;galement assur&#233;s que le contenu est compr&#233;hensible et navigable. Nous avons donc fourni :</p>

<ul>
	<li>une barre d&#8217;outils principale qui vous situe dans la structure de l&#8217;Universit&#233; &#8211; y compris des liens aux sites voulus dans la hi&#233;rarchie de l&#8217;organisation &#8211;,  des options &#171; Sauter au contenu &#187; ainsi que des options de r&#233;glage de la taille des polices. </li>
	<li>une structure de navigation normalis&#233;e dont les liens sont en texte et non en image, qui situe clairement l&#8217;utilisateur au sein des cinq niveaux d&#8217;information du site. </li>
	<li>un fil d&#8217;Ariane (chemin de navigation) localis&#233; au-dessus du titre de la page, qui permet &#233;galement &#224; l&#8217;utilisateur de se retrouver dans la structure du site.</li>
</ul>

<p>Si vous souhaitez nous faire part de votre opinion sur l&#8217;implantation de l&#8217;accessibilit&#233; sur ce site Web, veuillez envoyer vos commentaires aux Communications Web de Concordia sur notre <a href="http://www.concordia.ca/about/contactus/feedback.php">formulaire en ligne</a>. Toute personne utilisant des appareils et accessoires fonctionnels devrait communiquer avec le <a href="http://supportservices.concordia.ca/disabilities/">Service d&#8217;int&#233;gration des &#233;tudiants en situation de handicap</a> afin d&#8217;obtenir de l&#8217;aide pour les technologies d&#8217;assistance.</p>


<h2>Service d&#8217;int&#233;gration des &#233;tudiants en situation de handicap</h2>
<p>Le <a href="http://supportservices.concordia.ca/disabilities/">Service</a> offre des programmes et des services qui soutiennent l&#8217;int&#233;gration au sein de la communaut&#233; universitaire des &#233;tudiants &#224; mobilit&#233; r&#233;duite ou souffrant d&#8217;une d&#233;ficience visuelle ou auditive, ou encore de troubles d&#8217;apprentissage.</p>

Services offerts :<br />
<ul>
	<li>s&#233;ances d&#8217;accueil</li>
	<li>consultation et soutien p&#233;dagogique individualis&#233;s</li>
	<li>solutions de rechange pour la transcription</li>
	<li>services de lecteurs b&#233;n&#233;voles</li>
	<li>changements de salles de classe pour les &#233;tudiants &#224; mobilit&#233; r&#233;duite</li>
	<li>magn&#233;tophones, syst&#232;mes MF et ordinateurs personnels avec synth&#233;tiseur vocal et sortie braille pour utilisation sur le campus.</li>
</ul>

<h2>Politique officielle</h2>
<p><a href="http://vpexternalsecgen.concordia.ca/fr/documents/policies/VPS-14.pdf">La Politique &#8211; Accessibilit&#233; des &#233;tudiants handicap&#233;s aux &#233;tudes et services universitaires</a> (PDF) expose les r&#244;les et responsabilit&#233;s respectifs de l&#8217;Universit&#233; et des &#233;tudiants en situation de handicap quant &#224; l&#8217;accessibilit&#233; aux programmes universitaires et aux installations physiques de Concordia.</p>




