<h2>Outils d&#8217;accessibilit&#233;</h2>

<h3 id="textsize_faq">Tailles de la police</h3>

Dans le coin droit de chaque page, vous trouverez un lien vers trois tailles de police diff&#233;rentes : 
<ul>
	<li>moyen (r&#233;glage par d&#233;faut)</li>
	<li>grand</li>
	<li>tr&#232;s grand</li>
</ul>

<p>Ces r&#233;glages vous permettront d&#8217;augmenter la taille de la police de toutes les pages &#224; condition que Javascript soit activ&#233; et que les t&#233;moins (&#171; cookies &#187;) soient permis. Si Javascript est d&#233;sactiv&#233;, vous pouvez accro&#238;tre la police &#224; l&#8217;aide de votre navigateur. </p>

<h3>Raccourcis</h3>

Les raccourcis clavier pour ce site sont :
<ul>
	<li>s - Passer au contenu</li>
	<li>1 - Page d&#8217;accueil</li>
	<li>4 - Recherche</li>
	<li>0 - Renseignements sur les raccourcis clavier</li>
</ul>

<p>Utilisation des raccourcis clavier selon votre navigateur</p>

<ul>
	<li>Internet Explorer 5+ (PC) &#8211; Maintenir enfonc&#233;e la touche ALT tout en appuyant sur le raccourci clavier; rel&#226;cher les deux touches et appuyer sur RETOUR</li>
	<li>Internet Explorer 4 (PC) &#8211; Maintenir enfonc&#233;e la touche ALT tout en appuyant sur le raccourci clavier</li>
	<li>Internet Explorer 5+ (Mac) &#8211; Maintenir enfonc&#233;e la touche CTRL tout en appuyant sur le raccourci clavier</li>
	<li>Internet Explorer 4.5 (Mac) &#8211; Netscape six et les versions pr&#233;c&#233;dentes (PC et Mac)  n&#8217;acceptent pas les raccourcis clavier</li>
	<li>Netscape 7 (PC) &#8211; Maintenir enfonc&#233;e la touche ALT tout en appuyant sur le raccourci clavier</li>
	<li>Firefox, Mozilla (PC) &#8211; Maintenir enfonc&#233;es les touches Majuscule et ALT tout en appuyant sur le raccourci clavier</li>
	<li>Firefox, Mozilla (Mac) &#8211; Maintenir enfonc&#233;e la touche CTRL tout en appuyant sur le raccourci clavier</li>
	<li>Safari et Omniweb (Mac) &#8211; Maintenir enfonc&#233;e la touche CTRL tout en appuyant sur le raccourci clavier</li>
	<li>Opera &#8211; Maintenir enfonc&#233;e la touche Majuscule et appuyer sur &#201;chap. Rel&#226;cher les deux touches puis appuyer sur le raccourci clavier </li>
</ul>




