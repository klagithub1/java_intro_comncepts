			<h1>Sociology</h1>
		</div>
		<div class="main_content span_13 alpha">
      				<!-- InstanceBeginEditable name="MainContent" -->
  <p> Sociology is devoted to an understanding of social structures and   processes, the many institutions that humankind has formed as well as   the various patterns of human behaviour and social interaction   (cooperation, competition, conflict).</p>
  <p>It is based on the fact that human beings are social creatures,   and as such, live and die as members of and contributors to common   social traditions, and that society exists as an organized body of   attitudes, beliefs, and patterns of behaviour, that are amenable to   systematic observation, investigation, and analysis by means of reliable   methods and techniques.</p>
  <p>As an academic discipline, Sociology is a well-established   field of study. As an intellectual enterprise, Sociology makes a   substantial contribution to a Liberal Arts education. Knowledge of the   field enables the student to think critically and analytically about   problems which are part of his or her own society. His/Her willingness   and ability to think about the reasons for poverty, prejudice and other   social problems are enhanced and increased through exposure to   Sociology.  </p>
  <h3>Honours in Sociology &mdash; 60 credits</h3>
  <p>Designed for students who want to specialize in studying social  processes and acquire a strong theoretical and methodological  training. The Honours seminar provides students with a unique  opportunity to conduct and present their own research during  their final year of study. </p>
  <h3>Specialization in Sociology &mdash; 60 credits</h3>
  <p>Intended for students interested in acquiring and developing a  high level of skill and knowledge in studying societies and the  process of social change. The program provides exposure to an  array of theoretical perspectives and covers a variety of methodological  approaches.</p>
  <h3>Major in Sociology &mdash; 42 credits</h3>
  <p>Intended for students interested in acquiring a good level of skill  and knowledge to study societies and the process of social change.</p>
  <h3>Minor in Sociology &mdash; 30 credits</h3>
Provides students with basic knowledge in studying societies and  the process of social change.
<p>See the <a href="../degrees/bachelor-of-baccalaureate-in-arts-ba/index.html">BA degree</a> description for information on the core and elective requirements. For a complete list of courses: <a href="http://registrar.concordia.ca/calendar/">Undergraduate Calendar</a> </p><h3>Sample courses in Sociology and Anthropology include:</h3>
<p>Sociology of Culture,  Juvenile Crime and Delinquency,  Contemporary Sociological Theory,  Development Debates,  Introduction to Culture,  Race and Ethnic Relations,  Art, Aesthetics and Anthropology,  Peoples and Cultures of Oceania,  Population and Environment,  Research Design and Analysis,  Field Research,  Urban Regions,  Political Anthropology,  Experiments and Experience in Ethnographic Writing,  Anthropology of Food</p>
