			<h1>Admission Requirements for Computation Arts</h1>
		</div>
		<div class="main_content span_13 alpha">
      				<!-- InstanceBeginEditable name="MainContent" -->
  <h3>Program Type</h3>
  <p>Quota Program: Entry is competitive and selective, and the number          of qualified applicants may exceed the number of places available.          Applicants who meet the stated minimum requirements are          not guaranteed admission to these programs. Closing dates for          applications are stringently applied.</p>
  <h3>Program Availability</h3>
  <p>Admission to this program is only available for the  Fall Session.</p>
  <h3>Annual Capacity</h3>
  <p>40 (all Computation Arts program)</p>
  <h3>Concentrations </h3>
  <ul>
    <li>Specialization</li>
    <li>Major, <i>The Major in Computation Arts program must be   taken in combination with the Computer Applications Option program   offered by the Faculty of Engineering and Computer Science.</i></li>
    <li>Minor<br>
    </li>
  </ul>
  <h3>Cegep profile</h3>
  <ul>
    <li><strong>Specialization</strong><br>
      No specific profile required. However, following a profile in the arts              may help applicants build a successful portfolio.</li>
    <li><strong>Major</strong><br>
      Mathematics 103 or 201-NYA <strong>and</strong> 203 or 201-NYB <strong>and</strong> 105 or 201-NYC</li>
    <li><strong>Minor</strong><br>
      No specific profile required. However, following a profile in the arts        may help applicants build a successful portfolio. </li>
  </ul>
  <div class="hr_1_line"></div>
  <h3>Additional Requirements</h3>
  <p>Applicants must submit the following directly to the Department of Design &amp; Computation Arts:</p>
  <ul>
    <li> portfolio</li>
    <li> letter of intent</li>
    <li> photocopy of most recent academic record(s)</li>
    <li> letter of reference (optional)</li>
    <li>resum&eacute;/curriculum vitae (optional)</li>
  </ul>
  <p><strong>Portfolio Submission Deadline: March 1</strong><br />
    </p>
  <ul>
    <li> Applicants must apply online at <a href="https://connect2.concordia.ca/" target="_blank">Connect2.Concordia.ca</a> before submitting a portfolio.</li>
  </ul>
  <div class="hr_1_line"></div>
  <p><strong>Portfolio</strong></p>
  <p>A maximum of 15 pieces of work from the following three categories: </p>
  <p><strong> 1. Multimedia</strong></p>
  <ol type="a">
    <li> Digital or interactive media: 2D image modeling, 3D modeling            and animation, interactive work such as Flash, Processing,            Max, sound or original music, games</li>
    <li>Digital or interactive media that highlights programming or            scripting, hardware/electronics, software/technical skill</li>
  </ol>
  <p><strong>2. Two-Dimensional</strong></p>
  <ol type="a">
    <li>Still Images: graphic design, painting, drawing, photography,            typography, print media/printmaking, posters, technical and/or            expressive drawing, visual poetry and/or creative writing</li>
    <li> Moving Images: including film, video, animation</li>
  </ol>
  <p><strong>3. Three-Dimensional</strong></p>
  <ul>
    <li>3D object: sculpture, furniture, mixed media objects, clothing,            fibres design, body wearables, models and maquettes, ceramics,            moldmaking</li>
  </ul>
  <p><strong>Applicants to Computation Arts</strong> should focus their portfolio on digital and interactive media work, although work from the other categories will be considered. Applicants are encouraged to include work which demonstrates programming, scripting and/or electronics, regardless of whether the work is media-based.</p>
  <p>The <strong>letter of intent</strong> should:</p>
  <ul>
    <li> address the applicantâ¬"s reasons for applying to the program and summarize the applicantâ¬"s studio/academic/work experience, goals;</li>
    <li> summarize the applicantâ¬"s level of digital software skills, image- and object-making education/experience;</li>
    <li> summarize the applicantâ¬"s understanding of the nature and goals of the program (Design and/or Computation Arts) to which they are applying;</li>
    <li> be no longer than one page, written in English or French with attention to grammar, punctuation and quality of writing.</li>
  </ul>
  <p><strong>Academic Records</strong></p>
  <ul>
    <li>These should include, where applicable, high school, community            college, Cegep and university transcripts. This is a student copy of            the transcript(s), not the official copy sent to the Application Centre.</li>
  </ul>
  <p><strong>Letter of Reference (optional)</strong></p>
  <ul>
    <li>Applicants may include letters of reference from teachers and/or            employers. Students who have taken courses within the Department            of Design and Computation Arts prior to this application should            request letters of reference from their instructor(s).</li>
  </ul>
  <p><strong>ResumÃ©/Curriculum Vitae (optional)</strong></p>
  <p>Applicants may include a resumÃ©/curriculum vitae.</p>
  <div class="hr_1_line"></div>
  <p><strong>General Guidelines</strong></p>
  <ul>
    <li> Works larger than 20â¬ý x 24â¬ý must be presented as slides, photographs or on CD-ROM.</li>
    <li> DVDs and USB keys are accepted.</li>
    <li> Websites or HTML-based work must be burned to CD-ROM to avoid connection or server faults and long download times.</li>
    <li> CD-ROMs must clearly indicate which operating system (Mac or PC only) they require.</li>
    <li> Do not put paper labels on CDs or DVDs. Use a permanent marker to label your disks.</li>
    <li> Work submitted on CD or DVD must be accessible via an index HTML page or otherwise organized for easy, fast viewing.</li>
    <li> Websites or HTML-based portfolios must include a clearly visible HTML index page.</li>
    <li> Each work must be clearly numbered and labelled with the applicantâ¬"s name, the title of the work, the date of completion, the size of work, and the medium(s) used.</li>
    <li> In the case of collective or collaborative projects, applicants must <strong>clearly</strong> define their contribution to the project.</li>
    <li> An index must be included of all material submitted for review.</li>
    <li> Applicants may construct their own innovative portfolio packaging/container, but all works must be secured within the container and consideration should be given to easy access, review and storage.</li>
    <li> Acceptable portfolio size is 18â¬ýâ¬&xâ¬&24â¬ý or smaller.</li>
    <li> Mailed portfolios may be condensed in format, weight and size, and packaged for easy shipment. </li>
  </ul>
  <h3>The Department reserves the right to refuse any portfolios  that do not conform to the guidelines.</h3>
  <div class="hr_1_line"></div>
  <p><strong>Portfolio Retrieval</strong></p>
  <p>Applicants who wish to have their portfolios returned may</p>
  <ul>
    <li> arrange for the return of their portfolio by including a self-addressed            stamped envelope or a prepaid courier invoice which fully covers the            cost for return<br />
      <strong>or</strong></li>
    <li> pick up their portfolios in person from the Department between            April 15 and October 31 of the application year.</li>
  </ul>
  <p>The Department will discard any unclaimed portfolios.</p>
  <p><strong>Send Portfolio to:</strong></p>
