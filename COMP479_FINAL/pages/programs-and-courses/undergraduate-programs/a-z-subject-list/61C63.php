    <li><strong>Quebec Cegep (<a target="_blank" href="http://www.concordia.ca/admissions/undergraduate/admission-requirements/selection-and-next-steps/#crc">CRC</a>):</strong> DEC</li>
    <li><strong>Canadian High Schools:</strong> 65% or C+</li>
    <li><strong>External Transfers: </strong>C</li>
    <li><strong>International Bacc. (IB):</strong> A minimum total of 27 points. Admission to competitive programs is based on strong
      portfolio/auditions and, normally, above-average grades.</li>
