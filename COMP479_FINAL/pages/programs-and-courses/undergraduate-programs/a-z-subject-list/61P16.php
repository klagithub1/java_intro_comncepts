			<h1>Admission Requirements for Photography</h1>
		</div>
		<div class="main_content span_13 alpha">
      				<!-- InstanceBeginEditable name="MainContent" -->
  <h3>Program Type</h3>
  <p>Quota Program: Entry is competitive and selective, and the number  of qualified applicants may exceed the number of places available.  Applicants who meet the stated minimum requirements are  not guaranteed admission to these programs. Closing dates for  applications are stringently applied.        Departmental recommendation needed.</p>
  <h3>Program Availability</h3>
  <p>Admission to this program is only available for the  Fall Session.</p>
  <h3>Annual Capacity</h3>
  <p>50</p>
  <h3>Concentrations </h3>
  <ul>
    <li>Major</li>
    <li>Minor <i>(The Minor in Photography has been suspended for admission for fall 2012.)</i></li>
  </ul>
  <h3>Cegep profile</h3>
  <ul>
    <li>No specific profile required. However, following a profile in the arts may help applicants build a successful portfolio.</li>
  </ul>
  <div class="hr_1_line"></div>
  <h3>Additional Requirements</h3>
  <p>Applicants  must submit the following directly to the Department of Studio Arts:</p>
  <ul>
    <li>portfolio</li>
    <li>portfolio information form</li>
    <li>letter of intent</li>
    <li>photocopy of  most recent academic            record(s)</li>
  </ul>
  <p><strong>Portfolio Submission Deadline: March 1</strong><br />
    </p>
  <p>Applicants must apply online at <a href="https://connect2.concordia.ca/" target="_blank">Connect2.Concordia.ca</a> before submitting a portfolio.</p>
  <div class="hr_1_line"></div>
  <p>Only <strong>ONE</strong> portfolio and letter of intent is required if applying to more than one of these programs: <i>Studio Art, Art History &amp; Studio Art, Ceramics, Fibres, Painting &amp; Drawing, Print Media, Sculpture</i>.</p>
  <p><strong>Portfolio</strong><br>
    A portfolio of artwork for admission to a Studio Arts program should be a collection of your best and most recent work in visual arts. It should represent your individual
    interests, abilities and explorations. Your portfolio can include original work or digital documentation of work in two and/or three dimensions and time-based formats.
    Consider visual, conceptual, and technical proficiency in selecting your best and most recent work.</p>
  <p><strong>Your Work</strong></p>
  <ul>
    <li> Your portfolio should contain 15â¬20 images consisting of black-and-white and/or colour photographs.</li>
    <li> Prints are preferred to digital images as long as they are flat, unframed and measure no more than 24â¬ý x 36â¬ý (60 x 90 cm). CDs/DVDs or USB flash drives of
      JPEG images are also acceptable. Please see Digital Submissions for formatting guidelines.</li>
    <li> The Portfolio Evaluation Committee prefers cohesive, well-edited portfolios which indicate applicantsâ¬" specific interests and/or thought process.</li>
    <li> It is recognized that applicants may have limited training in the medium, therefore examples of work done in other visual arts media may be submitted to
      supplement the photographic portfolio.</li>
    <li> We do not accept any glassed or framed images.</li>
  </ul>
  <p>In addition to your portfolio of artwork, you must also include the following items (to be placed at the front of your
    portfolio): </p>
  <ul>
    <li> <strong>a <em>Portfolio Information form</em></strong><em></em><br>
      For mail-in portfolios, please print a PDF of the <a href="http://studio-arts.concordia.ca/documents/EVALUATIONS.pdf" target="_new">Portfolio Information Form</a> (for either Photography, Studio Arts or IMCA) available on the Studio Arts website
      (<a href="http://studio-arts.concordia.ca/">studio-arts.concordia.ca</a>). This form can also be filled out in person at the time of portfolio drop-off.<br />
    </li>
    <li><strong> a printed <em>Letter of Intent</em></strong><em></em><br>
      Your letter of intent should be no longer than one page and written in English or French. In general, it should describe your educational background and any relevant
      experience in art, as well as your expectations in relation to the programs to which you are applying.<br />
      <strong>1.</strong> List all courses and workshops you have taken in photography (including where and when).<br>
      <strong>2.</strong> Discuss the conceptual unity of the portfolio contents and the context in which the work was produced (for example, in a course).<br>
      <strong>3.</strong> Discuss the particular area(s) within the field of photography which interest you.<br>
      <strong>4.</strong> Write a short text on your aspirations upon receiving your degree.</li>
    <li> <strong>your most recent <em>Transcripts</em></strong><em></em><br>
      A printed photocopy of only your most recent academic transcripts is required in addition to any transcripts that you may have already sent to the Admissions
      Application Centre.</li>
    <li><strong>your <em>Image List</em></strong><em></em><br>
      A numbered image list should specify the medium, dimensions and date of completion (unless it is a work in progress) for each work.</li>
  </ul>
  <div class="hr_1_line"></div>
  <p><strong>General Portfolio Tips</strong></p>
  <ul>
    <li> Your work should be presented with care, but the presentation should not distract from the work itself. If you are submitting digital images, take the time to
      document your work properly &mdash; your images should be high quality, in focus, well lit, and indicate scale when necessary.</li>
    <li> Do not include anything in the portfolio that is breakable, sharp, or framed.</li>
    <li> Do not glue, staple, or tape your required documents (e.g. portfolio information form, letter of intent) to your portfolio. These documents should be easily
      located at the front of your portfolio.</li>
    <li> Do not include official or original transcripts, or copies of your birth certificate and Canadian citizenship card in your portfolio &mdash; these documents should be sent
      directly to the Admissions Application Centre.</li>
    <li> It is not necessary to label every individual piece &mdash; your image list should contain all relevant information related to your work.</li>
  </ul>
  <p><strong>Digital Submissions</strong></p>
  <ul>
    <li> Digital portfolios can be submitted on MAC-compatible CD/DVD or USB flash drive along with a corresponding numbered image list. Please ensure your disk/
      USB key is easy to navigate and that your files are kept to a manageable size.</li>
    <li> File size is different than image size. Still images should be submitted as numbered JPEG files (e.g. 01_LastName_FirstName, 02_LastName_FirstName) with a
      resolution size of approximately 1024 x 768, 72 dpi. Each image should not exceed 3 MB. Video and sound files should not exceed 60 MB each; the maximum
      running time is 10 minutes total.</li>
    <li> Please do not submit PowerPoint presentations since these take too long to load and can be distracting when viewing your portfolio.</li>
    <li> Write your name, student ID number, and the program to which you are applying on the actual disk (not on the case) using permanent marker. Do not attach any
      type of adhesive labels to your disk since these can fall off, jam up disk drives or make the disk unreadable.</li>
  </ul>
  <p><strong>Image List</strong></p>
  <ul>
    <li>This is a one-page printed description list that corresponds numerically to each piece submitted in your portfolio. It should contain the following information:
      medium, size, date, duration (in case of time-based work). It is not necessary to provide a detailed description of the work.</li>
  </ul>
  <div class="hr_1_line"></div>
  <p><strong>Portfolio Retrieval</strong></p>
  <p>Applicants who wish to have their portfolios returned must</p>
  <ul>
    <li>make arrangements for the return of their portfolio by including a self-addressed stamped envelope or a prepaid courier invoice which fully covers the cost for
      return, or</li>
    <li> pick up their portfolios from the Studio Arts office (VA 250) once a final decision has been made on their application, but before October 31 of the current
      calendar year. Unclaimed portfolios will be discarded in early November.<br>
      <strong>Pick up hours:</strong> Monday to Friday 9:00 &ndash; 11:30 and 13:30 &ndash; 16:00 (Please note: University offices close at 13:00 on Fridays for summer hours)<br>
    </li>
  </ul>
  <p><strong>Portfolio Guidelines</strong></p>
  <p>Every precaution is taken by the Department in handling the portfolios; however, the Department takes no responsibility for loss or damage to portfolios submitted.</p>
  <p><strong>The Department reserves the right to refuse any portfolio that does not conform to these guidelines.</strong></p>
  <div class="hr_1_line"></div>
  <p><strong>Send Portfolio to:</strong></p>
