			<h1>Fibres</h1>
		</div>
		<div class="main_content span_13 alpha">
      				<!-- InstanceBeginEditable name="MainContent" -->
  <p><i><strong>Fall admission only</strong></i></p>
  <p>The Fibres program allows for a rigorous and comprehensive study  of the technical, conceptual and creative aspects of contemporary  art and textile production. Interdisciplinary in nature, the Fibres  program encourages a cross-fertilization of ideas, techniques  and technologies. The rich history and tradition of fibres serves  as a starting point from which to examine the medium in the  contemporary art milieu.</p>
  <p>Students work in a broad range of fibre structure processes          including hand construction, loom and off-loom woven structures          and experimental approaches to textile and fibre art. Classes are          small to promote personal expression and critical discussion and          the open studio format supports the development of each student's          body of work. Students are encouraged to pursue special interests          and to further their understanding of the philosophy and historical          tradition of the medium.</p>
  <p>Printing and Dyeing Textile courses examine a wide range          of approaches to textile surface treatment including silk-screen          printing, relief printing, discharge and resist dyeing, fabric          constructions and embellishment. A component of advanced          courses deals with digital media for textile design and rendering          form. The emphasis is on creative and exploratory approaches to          technique, and the use of materials as a medium of expression.  </p>
  <h3>Major in Fibres &mdash; 60 credits</h3>
  <p>See <a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/a-z-subject-list/61F04.php">Program-Specific Admission Requirements</a> &ndash; In addition to the general admission requirements, this program have   extra program-specific requirements. These additional requirements must   be arranged with the departments to which you are applying.</p>
  <p>See the <a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/degrees/bachelor-of-baccalaureate-in-fine-arts-bfa/">BFA degree</a> description for more information on general degree requirements. For a complete list of courses: <a href="http://registrar.concordia.ca/calendar/">Undergraduate Calendar</a></p>
  <h3>Sample courses include:</h3>
  <ul>
    <li>Fibre structures</li>
    <li>Textile printing and dyeing</li>
    <li>History of Textile Art and Aspects of the History of Textile Art</li>
    <li>Art History and Art Theory courses</li>
    <li>Studio art courses chosen from disciplines such as Ceramics,            Intermedia/Cyberarts, Painting and Drawing, Photography (limited            access), Print Media and Sculpture</li>
  </ul>
  <h3>Facilities</h3>
  <p><strong>Fibres Structures Studio:</strong> Drum carders, spinning wheels,  a needle felting machine, several 4-8-12 harness looms, a 24 harness  AVL Dobby loom with Compu-dobby and a double bed electronic  knitting machine.</p>
  <p><strong>Printing and Dyeing Studio:</strong> Two 8 meter   printing tables with          registration rails, a light table, a heat transfer machine,   screen coating/drying/exposing/reclaiming equipment, a well-stocked dye          lab equipped with natural gas burners as well as a highly   ventilated          wax room for surface applications. A darkroom is also   accessible.</p>
  <p><strong>Paper-Making Studio:</strong> Two 2lb capacity Hollander beaters, two          hydropulpers, a vacuum table, a hydraulic press and two sheet dryers.</p>
  <p><strong>Computer Lab:</strong> Equipped with Macs and a PC running specialized          software for textile design, a scanner, three printers including an          Epson Stylus Pro 4880 dedicated to dye sublimation.</p>
  <p>Fibres students also have access to several industrial and electronic          sewing machines including a machine for digital embroidery.  </p>
  <h3>Studio Arts Department</h3>
  <p>Studio Arts is the largest department in the Faculty of Fine   Arts, with a long-standing reputation as one of Canada&rsquo;s foremost   environments          for the study of visual art. Students can complete a BFA Major   in one of the Department&rsquo;s seven disciplines (Ceramics, Fibres,   Intermedia/          Cyberarts, Painting &amp; Drawing, Photography, Print Media,   Sculpture) or take the more general BFA Major in Studio Art program.   Introductory,          intermediate and advanced courses allow students to develop as   far as they wish within a given discipline. Each year, a variety of   special topics          courses provide the Department with the flexibility to respond   immediately to contemporary currents in the art world.  </p>
