			<h1>Integrative Music Studies</h1>
		</div>
		<div class="main_content span_13 alpha">
      				<!-- InstanceBeginEditable name="MainContent" -->
  <p><i><strong>Fall admission only</strong></i></p>
  <p>The Major in Integrative Music Studies is particularly suited to individuals who wish to study a wide range of  music disciplines, this program provides the opportunity for both  flexibility and depth of study. First.year courses may include Theory/  Aural Training, Private Study, Music History, Music electives and  general electives.</p>
  <p>The balance of the Major consists of credits in Music          Composition, Aural Perception, Music History, Choir, additional          music electives and elective courses from other faculties. Music          electives may be selected from performance courses in both jazz          and classical areas, as well as courses in theory, arranging and          composition, including electroacoustics and music technology,          and music history.</p>
  <p>Students are initially accepted into  the Major in Integrative   Music Studies. Following successful completion of the first-year core   courses, students in the Major in Integrative Music Studies may apply   for transfer into one of the Specializations offered by the department:</p>
  <ul>
    <li><a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/a-z-subject-list/BFA_Jazz.php">Jazz Studies</a></li>
    <li> <a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/a-z-subject-list/BFA_MusicComposition.php">Music Composition</a></li>
    <li><a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/a-z-subject-list/BFA_MusicPerformance.php">Music Performance Studies</a></li>
  </ul>

  <h3>Major in Integrative Music Studies &mdash; 54 credits</h3>
  <p>See <a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/a-z-subject-list/61I11.php">Program-Specific Admission Requirements</a> &ndash; In addition to the general admission requirements, this program have   extra program-specific requirements. These additional requirements must   be arranged with the departments to which you are applying.</p>
  <p>See the <a href="http://www.concordia.ca/programs-and-courses/undergraduate-programs/degrees/bachelor-of-baccalaureate-in-fine-arts-bfa/">BFA degree</a> description for more information on general degree requirements.  For a complete list of courses: <a href="http://registrar.concordia.ca/calendar/">Undergraduate Calendar</a></p>
  <h3>Sample courses include:</h3>
  <p>Melody and Counterpoint,  Music Composition,  Jazz Arranging,  Jazz Ensemble,  Jazz Improvisation,  Private Study</p>
  <h3>Performing Groups</h3>
  <p><strong>Jazz Ensembles</strong><br>
    Every year jazz ensembles - big band, combos, guitar ensembles,  jazz choir, small combos and jazz vocal repertoire classes - present  concerts drawn from the jazz repertoire and newly composed  works. Concerts by jazz improvisation classes and special projects  are also presented in the Oscar Peterson Concert Hall.</p>
  <p><strong>Chamber Ensembles</strong><br>
    Each year students perform a number of chamber music concerts  related to the chamber music courses offered by the Department  from all periods for voice and instruments: duos, trios and large  chamber groups.</p>
  <p><strong>Vocal Studies</strong><br>
    Concerts are given regularly by vocal students with works  drawn from Western European art music, operatic and jazz vocal  repertoire traditions.</p>
  <p><strong>University Choir</strong><br>
    There are two university choirs. Both the large choir and the  chamber choir perform several concerts each year with repertoire  drawn from the 16th to 21st centuries. On occasion, these are  presented with the University Orchestra. This choir encourages  entry of all members of the Concordia community (students,  faculty and staff) as well as the general public. An audition  may be required to establish fundamental qualifications.</p>
  <p><strong>Readings and Concerts of Jazz and Composition Students</strong><br>
    There are informal readings and annual concerts of works by  Concordia students in both the jazz and composition areas.</p>
  <p><strong>Electroacoustics Concerts</strong><br>
    For more than 25 years, and with more than 300 concerts produced,   Concordia has been nationally noted for the quality and diversity of its   electroacoustics presentations. These include works by Canadian and   international composers as well as young and emerging sound artists   using an 18 channel sound projection system.</p>
  <h3>Facilities</h3>
  <p>Courses are given in new state-of-the-art facilities in the   Molson Building on the Sir George Williams campus. Designed specifically   for the Department's needs by an acoustician, the spaces include smart   classrooms and studios with networked multimedia, mixing and playback   capabilities, a recording room, octophonic studio, keyboard lab and   soundproofed practice modules.</p>
  <p>More than 100 concerts and presentations take place each year   (along with numerous seminars and master classes) in the acoustically   and visually spectacular Oscar Peterson Concert Hall on the Loyola   campus. The hall seats 600 people and boasts a multi-track and digital   recording studio. It is a premier venue for student and faculty   presentations, internationally acclaimed jazz, classical and   electroacoustics concerts, broadcasts, and numerous cd recordings.</p>
  <h3>Music Department</h3>
  <p>The Department fosters a rich environment for students from          diverse backgrounds to perform, compose and improvise in   different          languages including jazz, classical and electroacoustics. The   focus on          contemporary music is informed by a diversity of historical   styles          and genres studied through analysis, history and performance.   Course structures are built around lectures, workshops, studios,          private study, ensembles, tutorials and practice sessions.</p>
